////----------------------FOR LOOP-------------////

for (let i = 0; i < 5; i++) {
    console.log(i);
}

////--------------------WHILE LOOP------------////

let i = 0;
while (i < 5) {
    console.log(i);
    i++;
}

////--------------------DO WHILE LOOP-----------/////

let j = 0;
do {
    console.log(j);
    j++;
} while (j < 5);
